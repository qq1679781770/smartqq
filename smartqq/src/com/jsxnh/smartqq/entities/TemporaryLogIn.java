package com.jsxnh.smartqq.entities;

public class TemporaryLogIn {

	private Integer id;
	private Integer user_id;
	private String ip;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer use_id) {
		this.user_id = use_id;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
}
